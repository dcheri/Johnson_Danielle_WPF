console.log('---------------- Loops -----------------');

var b = 12;

while (b > 0){
	console.log(b + ' kegs on the wall');
	b--;
}

console.log('---------------- Do While Loops -----------------');

var f = 11;

do{
	console.log(f + ' kegs on the wall');
	f--;
} while(f > 0);

console.log('---------------- For Loops -----------------');

for ( var s = 15; s > 0; s--){
	console.log(s + ' kegs on the wall');
}